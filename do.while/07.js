/*7. Imprimir números ímpares de 1 a 20
Use do while para imprimir todos os números ímpares de 1 a 20.*/

// definir o inicio como i = 1 e avançar de 2 em 2 ou imprimir o resto de divisão diferente de 2 = % 2 != 0;


i = 1;

do {
    console.log(`Número ímpares de 1 a 20: ${i}`);
    i = i + 2
} while (i < 20);
